int	ft_str_is_printable(char *str)
{
	int	c;

	c = 0;
	while (str[c] != 0)
		c++;
	if (c == 0)
		return (1);
	c = 0;
	while (str[c] != 0)
	{
		if (str[c] >= 32 && str[c] <= 126)
			c++;
		else
			return (0);
	}
	return (1);
}
